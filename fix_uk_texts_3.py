from pathlib import Path

replacements = {
    "Удалять объекты может только администратор": "Видаляти об'єкти може тільки адміністратор",
    "Загружаем объекты...": "Завантажуємо об'єкти...",

    "Ошибка создания задачи": "Помилка створення завдання",
    "Ошибка удаления задачи": "Помилка видалення завдання",
    "Не вдалося видалити задачу": "Не вдалося видалити завдання",
    "Только администратор может создавать задачи": "Тільки адміністратор може створювати завдання",

    "Все объекты": "Усі об'єкти",
    "объекты": "об'єкти",
    "объект": "об'єкт",
    "задачу": "завдання",
    "задачи": "завдання",
}

for path in Path("lib").rglob("*.dart"):
    text = path.read_text(encoding="utf-8")

    new_text = text
    for old, new in replacements.items():
        new_text = new_text.replace(old, new)

    if new_text != text:
        path.write_text(new_text, encoding="utf-8")
        print(f"fixed: {path}")

print("done")
