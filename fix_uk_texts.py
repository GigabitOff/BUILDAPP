from pathlib import Path

replacements = {
    "Планируется": "Планується",
    "В работе": "В роботі",
    "Завершён": "Завершено",

    "Все статусы": "Усі статуси",
    "Статус завданьи": "Статус завдання",
    "Статус задачи": "Статус завдання",
    "Статус задачи обновлён": "Статус завдання оновлено",
    "Статус завданьи обновлён": "Статус завдання оновлено",

    "Ошибка обновления статуса": "Помилка оновлення статусу",
    "Не удалось обновить статус": "Не вдалося оновити статус",

    "Выбери объект, статус и порядок вывода": "Оберіть об'єкт, статус і порядок виводу",
    "Рмени объект, статус или сбрось фильтр.": "Змініть об'єкт, статус або скиньте фільтр.",

    "Без статуса": "Без статусу",

    "Материалов пока нет. Нажми “Додати”, чтобы создать первый материал.": "Матеріалів поки немає. Натисни “Додати”, щоб створити перший матеріал.",
    "Нажми": "Натисни",
    "чтобы": "щоб",
}

for path in Path("lib").rglob("*.dart"):
    text = path.read_text(encoding="utf-8")

    new_text = text
    for old, new in replacements.items():
        new_text = new_text.replace(old, new)

    if new_text != text:
        path.write_text(new_text, encoding="utf-8")
        print(f"fixed: {path}")

print("done")
