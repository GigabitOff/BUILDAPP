from pathlib import Path

replacements = {
    "Не удалось загрузить объекты": "Не вдалося завантажити об'єкти",
    "Не удалось создать приглашение": "Не вдалося створити запрошення",
    "Не удалось удалить": "Не вдалося видалити",
    "Не удалось загрузить задачи": "Не вдалося завантажити завдання",
    "Не удалось создать задачу": "Не вдалося створити завдання",
    "Не удалось удалить задачу": "Не вдалося видалити завдання",
    "Не удалось загрузить фотозвіты": "Не вдалося завантажити фотозвіти",
    "Не удалось сохранить фото": "Не вдалося зберегти фото",
    "Не удалось удалить фото": "Не вдалося видалити фото",
    "Не удалось загрузить завданьи": "Не вдалося завантажити завдання",

    "Сначала выбери объект": "Спочатку обери об'єкт",

    "завданьи": "завдання",
    "завданьи обновлён": "завдання оновлено",
    "фотозвіты": "фотозвіти",
}

for path in Path("lib").rglob("*.dart"):
    text = path.read_text(encoding="utf-8")

    new_text = text
    for old, new in replacements.items():
        new_text = new_text.replace(old, new)

    if new_text != text:
        path.write_text(new_text, encoding="utf-8")
        print(f"fixed: {path}")

print("done")
